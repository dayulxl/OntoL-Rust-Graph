//! # 动作路由器
//!
//! 按语言前缀将解析后的动作路由到对应的执行引擎：
//!
//! | 前缀 | 路由目标 |
//! |------|---------|
//! | `rdfs:` / `owl2:` | DWL2 查询引擎 |
//! | `swrl:` | SWRL 规则引擎 |
//! | `sh:` | SHACL 验证引擎 |
//! | `rule:` | 推理方向设定 |
//! | `func:` | 自定义函数调用 |
//!
//! ## 架构约束
//!
//! - 路由器是纯路由层 — 不执行具体的推理逻辑
//! - 使用 [`language`] 模块的 [`classify_inference_prefix`] 做前缀分类
//! - 未识别的前缀 → 跳过 + 日志记录（宽容执行），不 panic

